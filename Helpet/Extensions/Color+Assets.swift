import SwiftUI

extension Color {
    enum Background {
        static let primary = Color("PrimaryBackgroundColor")

        static let secondary = Color("SecondaryBackgroundColor")

        static let tertiary = Color("TertiaryBackgroundColor")

        static let quaternary = Color("QuaternaryBackgroundColor")

        static let quinary = Color("QuinaryBackgroundColor")
    }

    enum Foreground {
        static let primary = Color("PrimaryForegroundColor")

        static let secondary = Color("SecondaryForegroundColor")

        static let tertiary = Color("TertiaryForegroundColor")

        static let quaternary = Color("QuaternaryForegroundColor")

        static let quinary = Color("QuinaryForegroundColor")
    }
}
